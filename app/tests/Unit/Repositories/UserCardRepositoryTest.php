<?php

namespace Tests\Unit\Repositories;

use App\Models\UserCard;
use App\Repository\Eloquent\UserCardRepository;
use Tests\TestCase;

class UserCardRepositoryTest extends TestCase
{
    /**
     * @var UserCardRepository
     */
    private $usercardRepository;

    /**
     * Set up the test environment.
     */
    public function setUp(): void
    {
        parent::setUp(); // TODO: Change the autogenerated stub
        $this->usercardRepository = new UserCardRepository(new UserCard());
    }

    /**
     * Getting all fields of UserCard
     */
    public function testAll(): void
    {
        $this->assertEquals(UserCard::all(), $this->usercardRepository->all());
    }

    /**
     * Checking of existing card
     */
    public function testCards(): void
    {
        $this->assertFalse($this->usercardRepository->cards(1));
    }

    /**
     * Getting all user cards
     */
    public function testCardIdByUser(): void
    {
        $this->assertIsInt(count($this->usercardRepository->cardIdByUser(1)));
    }

    /**
     * Creating card for user
     */
    public function testCreateNew(): void
    {
        $this->assertTrue($this->usercardRepository->createNew(1, 1));
        $this->usercardRepository->delete(1, 1);
    }

    /**
     * Deleting card from user
     */
    public function testDelete(): void
    {
        $this->usercardRepository->createNew(1, 1);
        $this->assertTrue($this->usercardRepository->delete(1, 1));
    }

    public function testCreate():void
    {
        $this->usercardRepository->create([
            'user_id' => '0',
            'card_id' => '100'
        ]);
        $this->assertTrue((bool) UserCard::where('user_id', 0)->get());
        UserCard::where('user_id', 0)->delete();
    }
}
